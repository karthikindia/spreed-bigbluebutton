<template>
	<div id="allowed_groups" class="videocalls section">
		<h2>{{ t('talk_bbb', 'Limit to groups') }}</h2>
		<p class="settings-hint">
			{{ t('talk_bbb', 'When at least one group is selected, only people of the listed groups can be part of conversations.') }}
		</p>
		<p class="settings-hint">
			{{ t('talk_bbb', 'Guests can still join public conversations.') }}
		</p>
		<p class="settings-hint">
			{{ t('talk_bbb', 'Users that can not use Talk anymore will still be listed as participants in their previous conversations and also their chat messages will be kept.') }}
		</p>

		<p class="allowed-groups-settings-content">
			<Multiselect v-model="allowedGroups"
				class="allowed-groups-select"
				:options="groups"
				:placeholder="t('talk_bbb', 'Limit app usage to groups.')"
				:disabled="loading"
				:multiple="true"
				:searchable="true"
				:tag-width="60"
				:loading="loadingGroups"
				:show-no-options="false"
				:close-on-select="false"
				@search-change="searchGroup" />

			<button class="button primary"
				:disabled="loading"
				@click="saveChanges">
				{{ saveButtonText }}
			</button>
		</p>
	</div>
</template>

<script>
import Multiselect from '@nextcloud/vue/dist/Components/Multiselect'
import axios from '@nextcloud/axios'
import debounce from 'debounce'
import { generateOcsUrl } from '@nextcloud/router'
import { loadState } from '@nextcloud/initial-state'

export default {
	name: 'AllowedGroups',

	components: {
		Multiselect,
	},

	data() {
		return {
			loading: false,
			loadingGroups: false,
			groups: [],
			allowedGroups: [],
			saveButtonText: t('talk_bbb', 'Save changes'),
		}
	},

	mounted() {
		this.loading = true
		this.allowedGroups = loadState('talk', 'allowed_groups')
		this.groups = this.allowedGroups
		this.loading = false

		this.searchGroup('')
	},

	methods: {
		searchGroup: debounce(async function(query) {
			this.loadingGroups = true
			try {
				const res = await axios.get(generateOcsUrl('cloud', 2) + `groups?offset=0&search=${encodeURIComponent(query)}&limit=20`)
				// remove duplicates and sort
				this.groups = [...new Set(res.data.ocs.data.groups)].sort(function(a, b) {
					return a.localeCompare(b)
				})
			} catch (err) {
				console.error('Could not fetch groups', err)
			} finally {
				this.loadingGroups = false
			}
		}, 500),

		saveChanges() {
			this.loading = true
			this.loadingGroups = true
			this.saveButtonText = t('talk_bbb', 'Saving â€¦')

			OCP.AppConfig.setValue('talk_bbb', 'allowed_groups', JSON.stringify(this.allowedGroups), {
				success: function() {
					this.loading = false
					this.loadingGroups = false
					this.saveButtonText = t('talk_bbb', 'Saved!')
					setTimeout(function() {
						this.saveButtonText = t('talk_bbb', 'Save changes')
					}.bind(this), 5000)
				}.bind(this),
			})
		},
	},
}
</script>

<style lang="scss" scoped>
